function [A] = affine_transformation3( x, xp)

X = [x(1,1) x(2,1) 1  0  0  0;
      0  0 0 x(1,1) x(2,1)  1;
      x(1,2) x(2,2) 1 0  0  0;
      0  0 0 x(1,2) x(2,2)  1;
      x(1,3) x(2,3) 1 0  0  0;
      0  0 0 x(1,3) x(2,3)  1;];
% a = [a11; a12; a13; a21; a22; a23]
x_prime = [xp(1,1);xp(2,1);xp(1,2);xp(2,2);xp(1,3);xp(2,3)];
a = x_prime' / X';

A = [a(1,1) a(1,2) a(1,3);
     a(1,4) a(1,5) a(1,6);
       0   0   1];

end
